  // Utility method to compare API user data and UI user data
  static void compareUserProfileData(
      Map<String, dynamic> apiData, Map<String, dynamic> uiData) {
    final differences = <String, Map<String, dynamic>>{};
    for (final key in apiData.keys) {
      if (uiData.containsKey(key)) {
        if (apiData[key] != uiData[key]) {
          differences[key] = {
            'api': apiData[key],
            'ui': uiData[key],
          };
        }
      } else {
        differences[key] = {
          'api': apiData[key],
          'ui': 'missing in UI',
        };
      }
    }
    for (final key in uiData.keys) {
      if (!apiData.containsKey(key)) {
        differences[key] = {
          'api': 'missing in API',
          'ui': uiData[key],
        };
      }
    }
    if (differences.isEmpty) {
      print('User profile data matches between API and UI.');
    } else {
      print('Differences found between API and UI user data:');
      differences.forEach((key, value) {
        print('Field: $key, API: ${value['api']}, UI: ${value['ui']}');
      });
    }
  }

  // Utility method to print user profile details
